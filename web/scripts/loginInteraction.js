document.getElementById("register-in").addEventListener("click", () => {
    x = document.getElementById("login");
    x.style.marginLeft = "-50%";
    y = document.getElementById("box");
    y.style.width = "400px"
    y.style.height = "700px"
    z = document.getElementById("register");
    z.style.marginLeft = "0%";

})
